    $('#submit').click(function (e) { 
        e.preventDefault();
        $.ajax({
            type:'post',
            url:'<?php echo url('Index/AddContact'); ?>',
            dataType:'json',
            data:{
                'name':$('#name').val(),
                'email':$('#email').val(),
                'need':$('#need').val(),
                'desc':$('#desc').val()
            },
            success:function(dat){
                if(dat.stat == 1){
                    layer.alert(dat.msg,{icon:6},function(){
                        var index = layer.open();
                        layer.close(index);
                        location.reload();
                    })
                }else{
                    layer.alert(dat.msg,{icon:5},function(){
                        var index = layer.open();
                        layer.close(index);
                        location.reload();
                    })
                }
            }
        })
    });
